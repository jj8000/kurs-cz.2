# [] grupa dopuszczonych znakow [abc] - jedna z literek
# \s - dowolny bialy znak
# \S - cos nie bedace bialym znakiem
# \d - cyfra
# \D - dowolny bialy znak
# .dowolny znal, fod -> kod99, kod65
# ^ -> zaczynajacy sie od ^H -> wyszukuje sytuacje, gdzie H jest na poczatku
# $ -> konczy sie na a$ -> wyszukuje sytuacje, gdzie a jest na koncu tekstu
# * wystepuje dowolna ilosc razy
# + wystepuje dowolna liczbe razy, min. 1
# ? wystepuje raz lub wcale
# {5} powtarza sie, wystepuje dokladnie 5 razy
# () grupowanie razem w wieksze wyrazenie
# | jedno lub drugie  a|b -> wystepuje litera a lub b

#tipy:
# [a-z] -> wszystkie male litery
# [A - Z] -> wszystkie duze litery
# [0-9] -> wszystkie cyfry
